    private void ajustarTipos(){
        var tiposEmbalagem = embalagemRepositorio.buscarTipos();
        var tiposNomes = new ArrayList<String>();
        
        for(var tipo : tiposEmbalagem){
            tiposNomes.add(tipo.nome());
        }
        
        tipoEmbalagemModelo = new DefaultComboBoxModel(tiposNomes.toArray());
    }
    
    private void ajustarProdutos(){
        var produtoServico = new ProdutoServico();
        var produtos = produtoServico.buscarTodos();
        var produtosNomes = new ArrayList<String>();
        
        for(var produto : produtos){
            System.out.println(produto.getNome());
            produtosNomes.add(produto.getNome());
        }
        caixaVazia = produtosNomes.isEmpty();
        ajustarProdutosModelo = new DefaultComboBoxModel(produtosNomes.toArray());
    }
    
