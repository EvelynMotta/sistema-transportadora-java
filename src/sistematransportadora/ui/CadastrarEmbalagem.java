    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       try {
            int id = Integer.parseInt(txtID.getText());
            var produtoServico = new ProdutoServico();
            var produto = produtoServico.buscarTodos()[cbxProduto.getSelectedIndex()];
            var empilhavel = cbEmpilhavel.isSelected();
            var tipo = embalagemRepositorio.buscarTipos()[cbxTipos.getSelectedIndex()];
            double altura = ((double)(int)sAltura.getValue());
            double largura = ((double)(int)sPeso.getValue());
            double comprimento = ((double)(int)sLargura.getValue());
            double peso = ((double)(int)sComprimento.getValue());      
            String obs = txtObs.getText();
            
            var dimensoes = new Dimensoes(altura, largura, comprimento);
            var embalagem = new Embalagem( id, tipo, produto, dimensoes, peso);
            embalagem.setEmpilhavel(empilhavel);
            embalagem.setObservacoes(obs);
           
            embalagemServico.cadastrarEmbalagem(embalagem);
       } catch(Exception e) {
            
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
            return;
       }
        JOptionPane.showMessageDialog(null, "Seu cadastro foi um sucesso!!!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
        limpar();
                
    }//GEN-LAST:event_btnCadastrarActionPerformed
    private void limpar(){
        txtID.setText("");
        cbxProduto.setSelectedIndex(0);
        cbEmpilhavel.setSelected(false);
        sAltura.setValue(0);
        sLargura.setValue(0);
        sComprimento.setValue(0);
        sPeso.setValue(0);
        cbxTipos.setSelectedIndex(0);
        txtObs.setText("");

    } 

