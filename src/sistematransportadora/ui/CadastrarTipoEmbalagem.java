    private void carregarTabela() {
        var tiposProdutos = embalagemRepositorio.buscarTipos();
        
        dataModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        dataModel.addColumn("Id");
        dataModel.addColumn("Nome do Tipo");
        dataModel.addColumn("Padr達o");
        
        for (var tipo : tiposProdutos) {
            acrescentarNaTabela(tipo);
        }
        
        embalagemTabela.setModel(dataModel);
        
        // Ajuste de tamanho das colunas
        var colunaId = embalagemTabela.getColumn("Id");
        colunaId.setMaxWidth(50);
        colunaId.setMinWidth(40);
        
        var colunaPadrao = embalagemTabela.getColumn("Padr達o");
        colunaPadrao.setMaxWidth(70);
        colunaPadrao.setMinWidth(55);
    }
    
    private void acrescentarNaTabela(EmbalagemTipo tipoEmbalagem) {
        dataModel.addRow(new Object[] {
            tipoEmbalagem.id(),
            tipoEmbalagem.nome(),
            tipoEmbalagem.isPadrao()
                ? "Sim"
                : "N達o"
        });
    }
    
