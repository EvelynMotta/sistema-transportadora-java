    private void btnCadastrarDialogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarDialogoActionPerformed
        try {
            var nomeTipo = txtTipoNome.getText().trim();
            var idTipo = embalagemServico.buscarIdValidaParaTipo();

            EmbalagemTipo tipo = new EmbalagemTipo(idTipo, nomeTipo, false);
            embalagemServico.cadastrarTipo(tipo);
            acrescentarNaTabela(tipo);
            
            txtTipoNome.setText("");
            cadastroDialogo.setVisible(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                null, e.getMessage(),
                "Erro!", JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnCadastrarDialogoActionPerformed

    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        try {
            var index = embalagemTabela.getSelectedRow();
            if (index == -1) {
                throw new Exception("Nenhum tipo de embalagem foi selecionado!");
            }
            
            // S坦 exibe a mensagem de confirma巽達o caso o tipo n達o seja padr達o,
            // ou seja, caso o tipo seja realmente apag叩vel.
            if (dataModel.getValueAt(index, 2) == "N達o") {
                var respostaDialog = JOptionPane.showConfirmDialog(
                    null, 
                    """
                    Ao apagar o tipo de embalagem, voc棚 tamb辿m apagar叩 todos as embalagens
                    relacionadas. Voc棚 deseja continuar?
                    """,
                    "Aten巽達o!",
                    JOptionPane.YES_NO_OPTION
                );
                if (respostaDialog != JOptionPane.YES_OPTION)
                    return;
            }
            
            var idTipo = (int) dataModel.getValueAt(index, 0);
            embalagemServico.apagarTipoPorId(idTipo);
            dataModel.removeRow(index);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                null, e.getMessage(),
                "Erro!", JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnApagarActionPerformed

